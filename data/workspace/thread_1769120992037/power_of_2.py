def power_of_two(n: int) -> int:
    """Calculate 2 raised to the power of n using Pythonic style and Python 3.13 syntax.

    Args:
        n (int): The exponent.

    Returns:
        int: 2^n.
    """
    return 2 ** n

# Example usage
if __name__ == "__main__":
    print(power_of_two(5))  # Output: 32